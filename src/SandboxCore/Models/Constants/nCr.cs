        public static ulong nCr(int n, int r)
        {
            if (r > n)
                return 0;
            if (n == 0 || n == r)
                return 1;
            if (n <= 52)
                return _nCr[n, r];
            if (n - r < r)
                return partFactorial(n, r) / partFactorial(n - r, 1);
            else
                return partFactorial(n, n - r) / partFactorial(r, 1);
        }

        public static ulong partFactorial(int x, int y = 1)
        {
            if (x == 0 || x == y)
                return 1;
            if (y > x)
                return 0;
            UInt64 result = 1;
            for (var i = y + 1; i <= x; i++)
                result *= (UInt64)i;
            return result;
        }

